# -*- coding: utf-8 -*-
"""Константы Hikvision SDK -- только реально используемые значения."""
from __future__ import annotations

from enum import IntEnum

from .errors import MV_OK  # реэкспорт для удобства

# ---------------------------------------------------------------------------
# Типы устройств
# ---------------------------------------------------------------------------
MV_GIGE_DEVICE: int = 0x00000001    # GigE-устройство
MV_USB_DEVICE: int = 0x00000004     # USB3 Vision устройство

# ---------------------------------------------------------------------------
# Максимальные размеры
# ---------------------------------------------------------------------------
MV_MAX_DEVICE_NUM: int = 256        # Максимальное количество устройств
INFO_MAX_BUFFER_SIZE: int = 64      # Максимальный размер информационного буфера

# ---------------------------------------------------------------------------
# Режимы доступа к устройству
# ---------------------------------------------------------------------------
MV_ACCESS_Exclusive: int = 1        # Эксклюзивный доступ
MV_ACCESS_Control: int = 3          # Управление (другие могут читать регистры)

# ---------------------------------------------------------------------------
# Trigger (режим триггера)
# ---------------------------------------------------------------------------
MV_TRIGGER_MODE_OFF: int = 0        # Триггер выключен
MV_TRIGGER_MODE_ON: int = 1         # Триггер включен

# ---------------------------------------------------------------------------
# Exposure (автоэкспозиция)
# ---------------------------------------------------------------------------
MV_EXPOSURE_AUTO_MODE_OFF: int = 0  # Автоэкспозиция выключена

# ---------------------------------------------------------------------------
# PixelType -- перечисление форматов пикселей
# ---------------------------------------------------------------------------


class PixelType(IntEnum):
    """Форматы пикселей камер Hikvision (только реально используемые)."""

    # Монохромные
    MONO8 = 17301505
    MONO8_SIGNED = 17301506
    MONO16 = 17825799

    # Bayer
    BAYER_GR8 = 17301512
    BAYER_RG8 = 17301513
    BAYER_GB8 = 17301514
    BAYER_BG8 = 17301515
    BAYER_GR10 = 17825804
    BAYER_RG10 = 17825805
    BAYER_RG12 = 17825809
    BAYER_GR12 = 17825808

    # Цветные (RGB / BGR)
    RGB8 = 35127316
    BGR8 = 35127317
    RGBA8 = 35651606
    BGRA8 = 35651607

    # YUV
    YUV422 = 34603039
    YUV422_YUYV = 34603058

    # Неопределённый
    UNDEFINED = -1
