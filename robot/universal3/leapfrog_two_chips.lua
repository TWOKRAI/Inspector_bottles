-- =====================================================================
--  Delta SCARA · DRAStudio (Lua/RL) — ДВЕ ФИШКИ «ЧЕХАРДОЙ» ПО ОСИ Y
--
--  Две круглые фишки Ø55 мм с зазором 20 мм между краями стоят в ряд
--  вдоль оси Y. X — постоянный. Робот по очереди берёт заднюю фишку,
--  поднимает её «в прыжке» выше соседней и переносит ЧЕРЕЗ неё на
--  свободный слот впереди. Пара так «шагает» вверх до верхней границы,
--  затем тем же приёмом возвращается обратно к нижней границе. Цикл.
--
--  Геометрия задаётся числами 55 и 20 — НЕ нужно обучать каждый слот.
--  Обучаем ТОЛЬКО одну точку 'p0' = нижняя фишка (слот 0) на поверхности,
--  на глубине захвата, с любой ориентацией R (фишка круглая — R неважен).
--  Координаты остальных слотов программа считает сама: Y = базаY + n*ШАГ.
--
--  ⚠️ Проверь на железе ЗНАК ШАГА по Y: если первые прыжки идут «не туда»
--     (за пределы стола) — поставь STEP отрицательным (см. ниже).
-- =====================================================================

-- =====================  ПАРАМЕТРЫ  ==================================
DIA   = 55            -- диаметр фишки, мм (справочно)
GAP   = 20            -- зазор между краями соседних фишек, мм
STEP  = DIA + GAP     -- шаг между ЦЕНТРАМИ слотов по Y = 75 мм
                      --   при неверном направлении заменить на: STEP = -(DIA + GAP)
SLOTS = 4             -- сколько слотов в ряд (границы: слот 0 .. слот SLOTS-1)
                      --   2 фишки + это число задаёт длину «коридора» (SLOTS-1)*STEP
LIFT  = 60            -- высота «прыжка» над фишкой, мм. ДОЛЖНА быть больше
                      --   высоты фишки, иначе зацепит соседнюю при переносе
GRIP  = 0.4           -- дожим вакуума/отпускания, сек
GRIP_DO = 1           -- выход схвата/вакуума (как DO(1) в твоей программе)

-- =====================  СКОРОСТИ  ===================================
Accur("ROUGH")
Override(10)          -- 10% — медленно/безопасно для отладки, потом можно поднять
SpdJ(100)
AccJ(100)
DecJ(100)
SpdL(2000)
AccL(25000)
DecL(25000)

-- =====================  ОДИН ПРЫЖОК (слот nf → слот nt)  ============
--  Берёт фишку из слота nf, поднимает на LIFT (выше соседней),
--  переносит по Y к слоту nt и опускает. X всё время = базаX.
function jump(nf, nt)
  local yf = BY + nf * STEP
  local yt = BY + nt * STEP
  -- над фишкой-источником (X фикс, Y источника, высота прыжка)
  WritePoint('p0', "X", BX); WritePoint('p0', "Y", yf); WritePoint('p0', "Z", BZ + LIFT); MovL('p0')
  -- опуститься на фишку
  WritePoint('p0', "Z", BZ); MovL('p0')
  DO(GRIP_DO, "ON"); DELAY(GRIP)                 -- захват
  -- прыжок вверх — выше соседней фишки
  WritePoint('p0', "Z", BZ + LIFT); MovL('p0')
  -- перелёт по Y НАД соседом к цели (на высоте LIFT)
  WritePoint('p0', "Y", yt); MovL('p0')
  -- опуститься на цель
  WritePoint('p0', "Z", BZ); MovL('p0')
  DO(GRIP_DO, "OFF"); DELAY(GRIP)                -- отпустить
  -- увести схват вверх
  WritePoint('p0', "Z", BZ + LIFT); MovL('p0')
end

-- =====================  СТАРТ  ======================================
RobotServoOn()
DO(2, "ON")                                       -- общий выход (как у тебя)
MovP('home')

-- Захват БАЗЫ: один раз съездить на обученную точку p0 (нижняя фишка,
-- слот 0) и считать её реальные координаты. Дальше всё считается от базы.
MovL('p0')
BX = RobotX()
BY = RobotY()
BZ = RobotZ()

-- =====================  ГЛАВНЫЙ ЦИКЛ  ===============================
--  Старт: фишка A в слоте 0, фишка B в слоте 1 (соседние, 75 мм).
--  Пара «шагает» вверх до верхней границы, затем возвращается вниз.
while true do
  -- идём ВВЕРХ: задняя фишка прыгает через переднюю на слот +2
  for k = 0, SLOTS - 3 do
    jump(k, k + 2)                                -- через соседа в слоте k+1
  end
  -- достигли верхней границы: пара в слотах (SLOTS-2, SLOTS-1)
  -- идём ВНИЗ: передняя фишка прыгает назад через заднюю на слот -2
  for k = SLOTS - 2, 1, -1 do
    jump(k + 1, k - 1)                            -- через соседа в слоте k
  end
  -- вернулись к нижней границе: пара снова в слотах (0, 1)
end
