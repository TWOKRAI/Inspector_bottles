# Полная таблица глифов (латиница + кириллица + цифры). Пробел — пустой глиф.
GLYPHS: dict[str, Glyph] = {**_DIGITS, **_LATIN, **_CYRILLIC, " ": []}


def glyph(ch: str) -> Glyph | None:
    """Штрихи глифа символа (заглавная нормализация) или None, если нет в таблице."""
    if ch in GLYPHS:
        return GLYPHS[ch]
    up = ch.upper()
    return GLYPHS.get(up)


def advance(ch: str) -> float:
    """Ширина ячейки символа в grid-единицах (для раскладки строки)."""
    up = ch.upper()
    return _ADVANCE.get(ch, _ADVANCE.get(up, _DEF_ADV))


def supported() -> str:
    """Все поддерживаемые символы (для справки/тестов)."""
    return "".join(sorted(GLYPHS))


def heart(samples: int = 72) -> list[list[tuple[float, float]]]:
    """Параметрическое сердце как ОДНА замкнутая полилиния, нормированная в grid.

    x = 16 sin³t, y = 13 cos t − 5 cos 2t − 2 cos 3t − cos 4t (классическая кривая).
    Нормируем: высота = CAP, центр в (advance/2, CAP/2), Y ВВЕРХ (как глифы).
    """
    n = max(8, int(samples))
    raw: list[tuple[float, float]] = []
    for i in range(n + 1):  # +1 — замкнуть кривую (последняя = первой)
        t = 2.0 * math.pi * (i / n)
        x = 16.0 * math.sin(t) ** 3
        y = 13.0 * math.cos(t) - 5.0 * math.cos(2 * t) - 2.0 * math.cos(3 * t) - math.cos(4 * t)
        raw.append((x, y))
    xs = [p[0] for p in raw]
    ys = [p[1] for p in raw]
    span = max(max(xs) - min(xs), max(ys) - min(ys)) or 1.0
    k = CAP / span  # вписать в высоту прописной
    cx = (max(xs) + min(xs)) / 2.0
    cy = (max(ys) + min(ys)) / 2.0
    return [[((x - cx) * k + CAP / 2.0, (y - cy) * k + CAP / 2.0) for (x, y) in raw]]


def heart_advance() -> float:
    """Ширина ячейки сердца в grid (= CAP, квадратная)."""
    return CAP
